"""
AI 提示词模板

集中管理所有 AI 验证码识别的提示词，便于维护和优化。
"""

# ============================================================================
# 点击验证码提示词
# ============================================================================

CLICK_CAPTCHA_PROMPT = """这是一个验证码图片，是一个 3x3 的图片网格。
提示文字是："{prompt_text}"

请识别出哪些图片符合提示文字的要求，并返回这些图片的位置。

图片位置说明：
- 行号（row）：从上到下，0、1、2
- 列号（col）：从左到右，0、1、2

例如：
- 左上角：[0, 0]
- 中间：[1, 1]
- 右下角：[2, 2]

返回格式（JSON）：
{{
  "positions": [[row1, col1], [row2, col2], ...]
}}

注意事项：
1. 仔细观察每个图片，确保符合提示文字的要求
2. 如果没有符合的图片，返回空数组：{{"positions": []}}
3. 只返回 JSON 格式，不要包含其他文字
4. 位置坐标必须是 0-2 之间的整数

请开始识别：
"""


# ============================================================================
# 滑块验证码提示词
# ============================================================================

SLIDER_CAPTCHA_PROMPT = """这是一个滑块验证码图片。

请识别出图片中缺口的位置（X 坐标）。

返回格式（JSON）：
{{
  "gap_x": <缺口的 X 坐标，整数>
}}

注意事项：
1. 仔细观察图片，找到明显的缺口或拼图块位置
2. X 坐标是从左到右的像素位置
3. 只返回 JSON 格式，不要包含其他文字
4. 坐标必须是正整数

请开始识别：
"""


# ============================================================================
# 旋转验证码提示词（预留）
# ============================================================================

ROTATE_CAPTCHA_PROMPT = """这是一个旋转验证码图片。

请识别出图片需要旋转的角度，使图片正立。

返回格式（JSON）：
{{
  "angle": <旋转角度，0-360 之间的整数>
}}

注意事项：
1. 角度为顺时针旋转的度数
2. 0 度表示图片已经正立
3. 只返回 JSON 格式，不要包含其他文字
4. 角度必须是 0-360 之间的整数

请开始识别：
"""


# ============================================================================
# 提示词格式化函数
# ============================================================================

def format_click_captcha_prompt(prompt_text: str) -> str:
    """
    格式化点击验证码提示词

    Args:
        prompt_text: 验证码提示文字（如 "请选择所有包含交通灯的图片"）

    Returns:
        格式化后的提示词
    """
    return CLICK_CAPTCHA_PROMPT.format(prompt_text=prompt_text)


def format_slider_captcha_prompt() -> str:
    """
    格式化滑块验证码提示词

    Returns:
        格式化后的提示词
    """
    return SLIDER_CAPTCHA_PROMPT


def format_rotate_captcha_prompt() -> str:
    """
    格式化旋转验证码提示词

    Returns:
        格式化后的提示词
    """
    return ROTATE_CAPTCHA_PROMPT
